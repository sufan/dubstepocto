extension CGSize {
    /**
     The size whose width and height are both one.
     */
    public static var one = CGSize(width: 1.0,height: 1.0)

    /**
     The size whose width and height are widescreen ratio.
     */
    public static var wide = CGSize(width: 16.0,height: 9.0)
}
